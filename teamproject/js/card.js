function dis1(){
    if($('#dis1').css('display') == 'none'){    //id가 dis1인 div가 none일 때(안보일 때) onclick되면
    $('#dis1').show();                             //dis1 div 보여주기 
     }
     else{                                             //none이 아니면
    $('#dis1').hide();                              //dis1 div 숨기기
    }
}
